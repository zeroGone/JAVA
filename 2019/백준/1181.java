/* 1181 단어 정렬
 * 알파벳 소문자로 이루어진 N개의 단어가 들어오면 아래와 같은 조건에 따라 정렬하는 프로그램을 작성하시오.
 * 길이가 짧은 것부터
 * 길이가 같으면 사전 순으로
 * 입력:
 * 첫째 줄에 단어의 개수 N이 주어진다. (1≤N≤20,000) 
 * 둘째 줄부터 N개의 줄에 걸쳐 알파벳 소문자로 이루어진 단어가 한 줄에 하나씩 주어진다.
 * 주어지는 문자열의 길이는 50을 넘지 않는다.
 * 출력:
 * 조건에 따라 정렬하여 단어들을 출력한다. 
 * 단, 같은 단어가 여러 번 입력된 경우에는 한 번씩만 출력한다.
 * */

import java.util.ArrayList;
import java.util.Comparator;
import java.util.Scanner;

public class Main {
	
	public static void main(String[] args){
		Scanner input = new Scanner(System.in);
		int N = input.nextInt();
		
		String[] words = new String[N];
		
		for(int i=0; i<words.length; i++) words[i]=input.next();
		
		ArrayList<String> values = new ArrayList<String>();
		
		for(String i:words) {
			if(!values.contains(i)) {//같은 단어가 나오면 생깜
				values.add(i);//추가하고
				values.sort(new Comparator<String>() {//정렬
					@Override
					public int compare(String o1, String o2) {
						if(o1.length()<o2.length()) return -1;//길이가 짧으면 -1
						else if(o1.length()==o2.length()) {//길이가 같으면
							for(int i=0; i<o1.length(); i++) {//단어하나하나비교
								if(o1.charAt(i)<o2.charAt(i)) return -1;
								else if(o1.charAt(i)>o2.charAt(i)) return 1;
							}
						}
						return 1;
					}
				});
			}
		}
		
		for(String i:values) System.out.println(i);
	}
}